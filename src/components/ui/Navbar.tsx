function NavLink({ item, pathname, mobile = false }: { item: typeof allNav[0]; pathname: string | null; mobile?: boolean }) {
  const isActive = pathname === item.href || pathname?.startsWith(item.href + '/')
  const base = mobile
    ? `flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
        isActive ? 'bg-blue-50 text-blue-700' : 'text-gray-600 hover:bg-gray-100'
      }`
    : `inline-flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-sm font-medium transition-colors ${
        isActive ? 'bg-blue-50 text-blue-700' : 'text-gray-500 hover:text-gray-700 hover:bg-gray-100'
      }`

  return (
    <Link href={item.href} className={base}>
      {item.icon}
      {item.label}
    </Link>
  )
}

